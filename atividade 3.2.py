def desenha_retangulo_irlanda():
    desenhar_ret(-60, -50, 180, 90, 'green')

def desenhar_outro_ret(x,y,larg,alt, color):
    t.pu()
    t.goto(x, y)
    t.pd()
    t.begin_fill()
    t.fillcolor(color)
    t.lt(90)
    t.fd(larg)
    t.rt(90)
    t.fd(alt)
    t.rt(90)
    t.fd(larg)
    t.rt(90)
    t.fd(alt)
    t.end_fill()

def desenhar_parte_final():
    desenhar_outro_ret(60, -50, 180, 90, 'orange')

